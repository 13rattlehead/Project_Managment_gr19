"""
Решение задачи планирования (Фаза 4)
Проект: Интернет-магазин спортивных товаров FitLife

Допущения:
- 1 рабочий день = 8 часов
- Команда: 2 человека
- Длительность задачи = max(часы участников) / 8
"""

import random
random.seed(42)

TASKS = [
    "Анализ требований",
    "Проектирование архитектуры",
    "Проектирование БД",
    "Проектирование UI",
    "Настройка окружения",
    "Backend: каталог",
    "Backend: заказы",
    "Backend: пользователи",
    "Frontend: каталог",
    "Frontend: корзина",
    "Frontend: checkout",
    "Интеграция оплаты",
    "Интеграция доставки",
    "Тестирование",
    "Деплой"
]

LABOR_HOURS = [
    (16, 8),
    (12, 4),
    (8, 0),
    (4, 8),
    (2, 4),
    (6, 12),
    (6, 10),
    (4, 6),
    (0, 12),
    (0, 8),
    (0, 8),
    (6, 4),
    (4, 4),
    (8, 8),
    (4, 2),
]

def durations(hours):
    return [max(1, max(h)//8) for h in hours]

durs = durations(LABOR_HOURS)
total_days = sum(durs)

print("Календарный план проекта FitLife\n")
for name, d in zip(TASKS, durs):
    print(f"{name:<30} — {d} дн")

print(f"\nОбщая длительность проекта: ~{total_days} рабочих дней")
